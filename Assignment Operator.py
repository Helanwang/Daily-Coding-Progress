

"""Addition reassignment"""
a = 5  # variable a will have a new value which is 15 after reassignment
b = 10
a += b  # Mean a + b, but reassigned the result of these into a.
print(a)

a = "Hello"
b = "World"
a += b
print(a)


"""subtraction reassignment"""
a = 5
b = 3
a -= b
print(a)  # Output = 2


"""Multiplication reassignment"""
a = 4  # new value of 20
b = 5
a *= b
print(a)


"""Division reassignment"""
a = 6  # new value of 2
b = 3
a /= b
print(int(a))

